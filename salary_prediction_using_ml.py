import pandas as pd
import numpy as np

data = pd.read_csv("/content/Salary_Data.csv")

data.head()

data.columns

data.describe()

data.isnull()

data.isnull().any()

data.isnull().sum()

import matplotlib.pyplot as plt
import seaborn as sns

